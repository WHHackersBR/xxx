package com.vi.data;
/**
 * Needed for the position number list, so Struts2 can iterate through it
 * 
 * @author Zhou Cunhua
 *
 */

public class PositionNr {

	private String positionNrAbbr;
	private String positionNrName;
	/**
	 * @return the positionNrAbbr
	 */
	public String getPositionNrAbbr() {
		return positionNrAbbr;
	}
	/**
	 * @param positionNrAbbr the positionNrAbbr to set
	 */
	public void setPositionNrAbbr(String positionNrAbbr) {
		this.positionNrAbbr = positionNrAbbr;
	}
	/**
	 * @return the positionNrName
	 */
	public String getPositionNrName() {
		return positionNrName;
	}
	/**
	 * @param positionNrName the positionNrName to set
	 */
	public void setPositionNrName(String positionNrName) {
		this.positionNrName = positionNrName;
	}
	
	
}
